/*
* \brief Calcula la suma de dos numeros
* \param int primer número
* \param int segundo número
* \return el resultado de la suma
*
*/
int suma(int, int);

/*
* \brief Calcula la resta de dos numeros
* \param int primer número
* \param int segundo número
* \return el resultado de la resta
*
*/
int resta(int, int);

/*
* \brief Calcula la division de dos numeros
* \param int primer número
* \param int segundo número
* \return el resultado de la division, si el segundo numero es 0 return 0
*
*/
float division(int, int);

/*
* \brief Calcula el producto de dos numeros
* \param int primer número
* \param int segundo número
* \return el resultado del producto
*
*/
int multiplicacion(int, int);

/*
* \brief Calcula el factorial de un numero
* \param int primer número
* \return el resultado del factorial
*
*/
int factorial(int);